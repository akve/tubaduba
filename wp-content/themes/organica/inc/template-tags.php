/**
 * Show top panel second message.
 *
 * @since  1.0.0
 *
 * @param  string $format Output formatting.
 *
 * @return void
 */
function organica_top_message( $format = '%s' ) {
	$is_enabled_icon = get_theme_mod( 'top_panel_icon_enable', organica_theme()->customizer->get_default( 'top_panel_icon_enable' ) );
	$is_enabled_link = get_theme_mod( 'top_panel_link_enable', organica_theme()->customizer->get_default( 'top_panel_link_enable' ) );
	$icon            = get_theme_mod( 'top_panel_icon', organica_theme()->customizer->get_default( 'top_panel_icon' ) );
	$link            = get_theme_mod( 'top_panel_link', organica_theme()->customizer->get_default( 'top_panel_link' ) );
	$message         = get_theme_mod( 'top_panel_text', organica_theme()->customizer->get_default( 'top_panel_text' ) );

	if ( $is_enabled_icon && $is_enabled_link ) {
		$content = sprintf( '<i class="fa %1$s"></i><a href="%2$s">%3$s</a>', $icon, $link, $message );
	} else if ( $is_enabled_icon ) {
		$content = sprintf( '<i class="fa %1$s"></i>%2$s', $icon, $message );
	} else if ( $is_enabled_link && $message ) {
		$content = sprintf( '<a href="%1$s">%2$s</a>', $link, $message );
	} else {
		$content = $message;
	}

	if ( ! $is_enabled_icon && ! $is_enabled_link && ! $message ) {
		return;
	}

	printf( $format, wp_kses( $content, wp_kses_allowed_html( 'post' ) ) );
}

